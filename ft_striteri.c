#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t			i;
	unsigned int	j;

	if (s && f)
	{
		j = 0;
		i = 0;
		while (s[i])
		{
			(*f)(j++, &s[i]);
			i++;
		}
	}
}
