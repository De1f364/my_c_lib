#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*str1;

	i = 0;
	str1 = s;
	if (!n)
		return ;
	while (i < n)
	{
		str1[i] = 0;
		i++;
	}
}
